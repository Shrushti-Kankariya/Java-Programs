// An Algorithm to print the area of rectangle

public class AreaOfRect{
	public static void main ( String[] args){
	
		int len = 20;
		int bre = 30;
		double area = len * bre;

		System.out.println(" The area of the rectangle for length " + len + " and breadth " + bre + " is  :" + area);
		
}

}