// An algorithm to print the square and cube of any number


public class SqrCube {
    public static void main(String[] args) {
	int no = 12;
	int s = no * no;
	int c = no * no * no;
	
        System.out.println("The square of the number is : " + s + " The cube of the number is : " + c);
	
    }
}
